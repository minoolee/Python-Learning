# Str
a = 'Some word'
print(type(a))

# Int
number = 7
print(type(number))

# float
flo = 14.7
print(type(flo))

# complex
com = 7j
print(type(com))
fir = 3 + 6j
sec = 6 + 8j
res = fir + sec
print(res)
print(fir-sec)

# list Array
lis = ["apple", "banana", "cherry"]
print(type(lis))
print(lis)

# tuple
tup = ("apple", "banana", "cherry")
print(type(tup))

# range
ren = range(7)
print(ren)
print(type(ren))

# dict obj
dic = {"name": "Chars", "age": 30}
print(dic)
print(type(dic))


# set Note: the set list is unordered, meaning: the items will appear in a random order.
sett = {"apple", "banana", "cherry"}
print(sett)

# frozenset
froze = frozenset({"apple", "banana", "cherry"})
print(froze)

# bool
boool = True
print(type(boool))

# bytes
byte = b"Hello"
print(type(byte))
print(byte)

# bytearray
bt = bytearray(7)
print(bt)


